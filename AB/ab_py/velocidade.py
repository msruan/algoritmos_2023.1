#1. Leia uma velocidade em m/s, calcule e escreva esta velocidade em km/h. (Vkm/h = Vm/s * 3.6)

#entrada
metros_segundos = float(input('Insira a velocidade em metros por segundo:'))

#proces
kilo_horas = metros_segundos * 3.6

#saída
print('Essa velocidade em quilômetros por hora é',kilo_horas,'km/h')